package workingwithabstraction.lab.workingwithabstractionlab.pointinrectangle;

public class Point {
    private int pointX;
    private int getPointY;

    public Point(int pointX, int getPointY) {
        this.pointX = pointX;
        this.getPointY = getPointY;
    }

    public int getPointX() {
        return pointX;
    }

    public int getGetPointY() {
        return getPointY;
    }
}
